void main(int argc, char *argv[]){  //compile with the following:   gcc -o led main.c 
  if (argc==2) { //only display something if we pass a command line parameter
	const unsigned char *p=(const unsigned char *)argv[1];
    for (int j=0;j<8;++j) { //font is 8 pixels tall
      for (int k=0;k<strlen(argv[1]);++k)  //draw X quantity of symbol 
        if (p[k]>='0' && p[k]<='9') { //only print if character is a number
          for (int l=0;l<8;++l) { //draw 8 pixels wide
            if ( FONT[p[k]-'0'][j] & ( 1 << (7-l)) )
              printf("#");
            else 
              printf(" ");
          }
        }
      printf("\n");
    }
  }
}
